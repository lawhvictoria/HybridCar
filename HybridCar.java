public class HybridCar {
        
    private int cost;
    private int miles;
    private double gas;
    private int mpg;
    private double resale;
    
    public HybridCar(int c, int m, double g, int mpg, double r)
    {
        this.cost = c;
        this.miles = m;
        this.gas = g;
        this.mpg = mpg;
        this.resale = r;
    }
}
